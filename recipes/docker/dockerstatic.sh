  rurl="${rburl}/aarch64/${rfile}"
  rsha256="2f291f561b6534ace74a357bd02676d6015ab83835c65b909f8a9422fa675a1d"
elif [[ ${karch} =~ ^arm ]] ; then
  rurl="${rburl}/armhf/${rfile}"
  rsha256="4fdb28043e746341f0bce4f1e2a7e275a570da89f6bed41a6bc59a7338ec66e2"
elif [[ ${karch} =~ ^x86_64 ]] ; then
