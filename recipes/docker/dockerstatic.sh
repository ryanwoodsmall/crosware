elif [[ ${karch} =~ ^arm ]] ; then
  rurl="${rburl}/armhf/${rfile}"
  rsha256="57ce980cf38eb125627b6a19801272a145583f99782bc487083671236ebc2bd1"
elif [[ ${karch} =~ ^x86_64 ]] ; then
